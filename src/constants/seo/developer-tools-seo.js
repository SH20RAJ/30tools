// SEO utility functions for developer tools
export const generateDeveloperToolMeta = (toolId) => {
  const tool = DEVELOPER_TOOLS_SEO.tools[toolId];
  if (!tool) return null;

  return {
    title: tool.title,
    description: tool.description,
    keywords: tool.keywords.join(", "),
    openGraph: {
      title: tool.title,
      description: tool.description,
      url: `https://30tools.com/${toolId}`,
      siteName: "30tools",
      images: [
        {
          url: `/og-images/${toolId}.jpg`,
          width: 1200,
          height: 630,
          alt: tool.title,
        },
      ],
      type: "website",
    },
    twitter: {
      card: "summary_large_image",
      title: tool.title,
      description: tool.description,
      images: [`/og-images/${toolId}.jpg`],
      creator: "@30tools",
    },
    alternates: {
      canonical: `https://30tools.com/${toolId}`,
    },
    robots: {
      index: true,
      follow: true,
      googleBot: {
        index: true,
        follow: true,
        "max-video-preview": -1,
        "max-image-preview": "large",
        "max-snippet": -1,
      },
    },
    category: "Developer Tools",
  };
};

