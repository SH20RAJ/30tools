// Helper function to get FAQs for a specific image tool
export const getImageToolFAQs = (toolId) => {
  const toolFAQs = IMAGE_TOOLS_FAQS[toolId] || [];
  const generalFAQs = IMAGE_TOOLS_FAQS.general || [];

  return [...toolFAQs, ...generalFAQs];
};

// Helper function to search image FAQs
export const searchImageFAQs = (query) => {
  const allFAQs = [];

  // Collect all FAQs
  Object.values(IMAGE_TOOLS_FAQS).forEach((faqs) => {
    if (Array.isArray(faqs)) {
      allFAQs.push(...faqs);
    }
  });

  // Search in questions and answers
  return allFAQs.filter(
    (faq) =>
      faq.question.toLowerCase().includes(query.toLowerCase()) ||
      faq.answer.toLowerCase().includes(query.toLowerCase())
  );
};

export default IMAGE_TOOLS_FAQS;
